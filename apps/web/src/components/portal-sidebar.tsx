interface PortalSidebarProps {
  locale: string;
  variant: 'dashboard' | 'coach';
}

export function PortalSidebar({ locale, variant }: PortalSidebarProps) {
  const pathname = usePathname();
  const isAr = locale === 'ar';
  const items = variant === 'dashboard' ? dashboardNav : coachNav;

  return (
    <nav className="w-full md:w-56 shrink-0">
      {/* Mobile: horizontal scroll */}
      <div className="flex md:flex-col gap-1 overflow-x-auto md:overflow-visible pb-2 md:pb-0 -mx-1 px-1">
        {items.map((item) => {
          const fullHref = `/${locale}${item.href}`;
          const isActive = pathname === fullHref || (item.href !== '/dashboard' && item.href !== '/coach' && pathname.startsWith(fullHref));

          return (
            <a
              key={item.href}
              href={fullHref}
              className={`flex items-center gap-2.5 px-3 py-2.5 rounded-lg text-sm font-medium whitespace-nowrap transition-colors min-h-[44px] ${
                isActive
                  ? 'bg-[var(--color-primary)] text-white shadow-sm'
                  : 'text-[var(--color-neutral-600)] hover:bg-white hover:text-[var(--color-primary)]'
              }`}
            >
              <svg className="w-5 h-5 shrink-0" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
                <path strokeLinecap="round" strokeLinejoin="round" d={item.icon} />
              </svg>
              <span>{isAr ? item.labelAr : item.labelEn}</span>
            </a>
          );
        })}
      </div>
    </nav>
  );
}
